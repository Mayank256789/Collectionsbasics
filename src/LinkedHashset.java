import java.util.LinkedHashSet;

public class LinkedHashset {
    public static void main(String[] args) {
        LinkedHashSet<Integer> set = new LinkedHashSet<Integer>();
        set.add((10));
        set.add((15));
        set.add((25));
        set.add((30));
        set.add((35));
        System.out.println(set);

    }
}
